#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t i;
	size_t j;
	size_t res;

	j = 0;
	i = 0;
	res = 0;
	while (dst[i] != '\0' && i < size)
		i++;
	res += i;
	while (src[res] != '\0')
		res++;
	if (dst[i] != '\0')
		i++;
	while (src[j] != '\0')
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (res);
}
