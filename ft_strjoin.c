#include "libft.h"

char * ft_strjoin(char const *s1, char const *s2)
{
	char *array;
	int i;
	int j;

	i = 0;
	j = 0;
	if (!s1 || !s2)
		return (NULL);
	array = ft_strnew(strlen(s1) + strlen(s2));
	if (!array)
		return (NULL);
	ft_strcpy(array, s1);
	ft_strcat(array, s2);
	return (array);
}
